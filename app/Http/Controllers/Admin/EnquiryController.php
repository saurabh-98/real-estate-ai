<?php

namespace App\Http\Controllers\Admin;

use App\Http\Controllers\Controller;

use Illuminate\Http\Request;

use Illuminate\Http\JsonResponse;

use App\Models\Enquiry;

use Yajra\DataTables\Facades\DataTables;

class EnquiryController extends Controller
{
    /*
    |--------------------------------------------------------------------------
    | ENQUIRY LIST
    |--------------------------------------------------------------------------
    */

    public function index(Request $request)
    {
        /*
        |--------------------------------------------------------------------------
        | AJAX DATATABLE
        |--------------------------------------------------------------------------
        */

        if ($request->ajax()) {

            $query = Enquiry::with('property')

                ->latest();

            return DataTables::of($query)

                /*
                |--------------------------------------------------------------------------
                | INDEX
                |--------------------------------------------------------------------------
                */

                ->addIndexColumn()

                /*
                |--------------------------------------------------------------------------
                | CHECKBOX
                |--------------------------------------------------------------------------
                */

                ->addColumn('checkbox', function ($row) {

                    return '

                        <input
                            type="checkbox"
                            class="row-checkbox"
                            value="' . $row->id . '"
                        >

                    ';
                })

                /*
                |--------------------------------------------------------------------------
                | CUSTOMER
                |--------------------------------------------------------------------------
                */

                ->addColumn('customer', function ($row) {

                    return '

                        <div class="customer-info">

                            <div class="customer-avatar">

                                ' . strtoupper(substr($row->name, 0, 1)) . '

                            </div>

                            <div>

                                <h6>

                                    ' . e($row->name) . '

                                </h6>

                                <span>

                                    ' . e($row->email) . '

                                </span>

                            </div>

                        </div>

                    ';
                })

                /*
                |--------------------------------------------------------------------------
                | PROPERTY
                |--------------------------------------------------------------------------
                */

                ->addColumn('property', function ($row) {

                    if ($row->property) {

                        return '

                            <div class="property-info">

                                <strong>

                                    ' . e($row->property->title) . '

                                </strong>

                            </div>

                        ';
                    }

                    return '

                        <span class="text-muted">

                            Property Removed

                        </span>

                    ';
                })

                /*
                |--------------------------------------------------------------------------
                | MOBILE
                |--------------------------------------------------------------------------
                */

                ->addColumn('mobile', function ($row) {

                    return '

                        <div class="contact-info">

                            <span>

                                ' . e($row->mobile) . '

                            </span>

                        </div>

                    ';
                })

                /*
                |--------------------------------------------------------------------------
                | STATUS
                |--------------------------------------------------------------------------
                */

                ->addColumn('status', function ($row) {

                    return $row->status_badge;
                })

                /*
                |--------------------------------------------------------------------------
                | DATE
                |--------------------------------------------------------------------------
                */

                ->addColumn('created_at', function ($row) {

                    return $row->formatted_date;
                })

                /*
                |--------------------------------------------------------------------------
                | ACTION
                |--------------------------------------------------------------------------
                */

                ->addColumn('action', function ($row) {

                    return '

                        <div class="action-buttons">

                            <a
                                href="' . route('admin.enquiries.show', $row->id) . '"
                                class="table-action-btn view-btn"
                            >

                                <i class="fa-solid fa-eye"></i>

                            </a>

                            <button
                                class="table-action-btn delete-btn deleteEnquiryBtn"
                                data-id="' . $row->id . '"
                            >

                                <i class="fa-solid fa-trash"></i>

                            </button>

                        </div>

                    ';
                })

                /*
                |--------------------------------------------------------------------------
                | RAW COLUMNS
                |--------------------------------------------------------------------------
                */

                ->rawColumns([

                    'checkbox',

                    'customer',

                    'property',

                    'mobile',

                    'status',

                    'action'

                ])

                /*
                |--------------------------------------------------------------------------
                | RESPONSE
                |--------------------------------------------------------------------------
                */

                ->make(true);
        }

        /*
        |--------------------------------------------------------------------------
        | NEW ENQUIRY COUNT
        |--------------------------------------------------------------------------
        */

        $newCount = Enquiry::new()->count();

        /*
        |--------------------------------------------------------------------------
        | RETURN VIEW
        |--------------------------------------------------------------------------
        */

        return view(

            'admin.enquiries.index',

            compact(

                'newCount'

            )

        );
    }

    /*
    |--------------------------------------------------------------------------
    | SHOW ENQUIRY
    |--------------------------------------------------------------------------
    */

    public function show($id)
    {
        /*
        |--------------------------------------------------------------------------
        | FIND ENQUIRY
        |--------------------------------------------------------------------------
        */

        $enquiry = Enquiry::with('property')

            ->findOrFail($id);

        /*
        |--------------------------------------------------------------------------
        | MARK AS READ
        |--------------------------------------------------------------------------
        */

        if ($enquiry->isNew()) {

            $enquiry->markAsRead();

            $enquiry->refresh();
        }

        /*
        |--------------------------------------------------------------------------
        | RETURN VIEW
        |--------------------------------------------------------------------------
        */

        return view(

            'admin.enquiries.show',

            compact('enquiry')

        );
    }

    /*
    |--------------------------------------------------------------------------
    | UPDATE STATUS
    |--------------------------------------------------------------------------
    */

    public function updateStatus(Request $request, $id): JsonResponse
    {
        /*
        |--------------------------------------------------------------------------
        | VALIDATION
        |--------------------------------------------------------------------------
        */

        $request->validate([

            'status' =>

                'required|in:new,read,replied'

        ]);

        /*
        |--------------------------------------------------------------------------
        | FIND ENQUIRY
        |--------------------------------------------------------------------------
        */

        $enquiry = Enquiry::findOrFail($id);

        /*
        |--------------------------------------------------------------------------
        | UPDATE
        |--------------------------------------------------------------------------
        */

        $enquiry->update([

            'status' => $request->status

        ]);

        /*
        |--------------------------------------------------------------------------
        | RESPONSE
        |--------------------------------------------------------------------------
        */

        return response()->json([

            'success' => true,

            'message' => 'Status updated successfully'

        ]);
    }

    /*
    |--------------------------------------------------------------------------
    | DELETE ENQUIRY
    |--------------------------------------------------------------------------
    */

    public function destroy($id): JsonResponse
    {
        /*
        |--------------------------------------------------------------------------
        | FIND
        |--------------------------------------------------------------------------
        */

        $enquiry = Enquiry::findOrFail($id);

        /*
        |--------------------------------------------------------------------------
        | DELETE
        |--------------------------------------------------------------------------
        */

        $enquiry->delete();

        /*
        |--------------------------------------------------------------------------
        | RESPONSE
        |--------------------------------------------------------------------------
        */

        return response()->json([

            'success' => true,

            'message' => 'Enquiry deleted successfully'

        ]);
    }

    /*
    |--------------------------------------------------------------------------
    | BULK DELETE
    |--------------------------------------------------------------------------
    */

    public function bulkDelete(Request $request): JsonResponse
    {
        /*
        |--------------------------------------------------------------------------
        | VALIDATION
        |--------------------------------------------------------------------------
        */

        $request->validate([

            'ids' => 'required|array',

            'ids.*' => 'exists:enquiries,id'

        ]);

        /*
        |--------------------------------------------------------------------------
        | DELETE
        |--------------------------------------------------------------------------
        */

        Enquiry::whereIn(

            'id',

            $request->ids

        )->delete();

        /*
        |--------------------------------------------------------------------------
        | RESPONSE
        |--------------------------------------------------------------------------
        */

        return response()->json([

            'success' => true,

            'message' => 'Selected enquiries deleted successfully'

        ]);
    }

    /*
    |--------------------------------------------------------------------------
    | MARK ALL READ
    |--------------------------------------------------------------------------
    */

    public function markAllRead(): JsonResponse
    {
        /*
        |--------------------------------------------------------------------------
        | UPDATE
        |--------------------------------------------------------------------------
        */

        Enquiry::where(

            'status',

            Enquiry::STATUS_NEW

        )->update([

            'status' => Enquiry::STATUS_READ

        ]);

        /*
        |--------------------------------------------------------------------------
        | RESPONSE
        |--------------------------------------------------------------------------
        */

        return response()->json([

            'success' => true,

            'message' => 'All enquiries marked as read'

        ]);
    }
}